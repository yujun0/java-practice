package com.sample.packagea;

public class ClassA {

    private int w = 1;
    int x = 2;
    protected int y = 3;
    public int z = 4;

    public void display() {
        System.out.println("ClassA...");
        System.out.println("- w = " + w);
        System.out.println("  x = " + x);
        System.out.println("# y = " + y);
        System.out.println("+ z = " + z);
    }

}
